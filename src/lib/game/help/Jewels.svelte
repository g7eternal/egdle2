<p>A twist of a popular casual matching game: combine three or more elements to remove them from the grid.</p>

<hr />
<img src="example/jewels.webp" alt="Example - Egdle Crush" />
<hr />

<p>
  Click two adjacent cells to swap the elements. Or move the cell with a single swipe, if you're using a touch screen.
  <br />
  Elements cannot be swapped diagonally.
</p>

<p>
  Once three or more of the same elements are placed in a row or a column, they are destroyed. You earn points by doing
  exactly that - clearing up the field. Build longer combos for better rewards!
</p>

<p>
  There is a timer in the top of your screen. As soon as it runs out - the game ends. Refill the timer by earning
  points, and try not to run out of available moves.
</p>

<p>
  <strong>How long can you keep up? Share your best scores with friends!</strong>
</p>

