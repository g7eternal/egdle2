<p>
  This game is a variation of a "15" puzzle. Your goal is to assemble the picture by moving pieces around using an empty
  space.
</p>

<hr />
<img src="example/puzzle.webp" alt="Example - Puzzle" />
<hr />

<p>
  Click on a cell to slide it into the free space. Or flick it with a swipe, if you're using a touch screen.
  <br />
  Elements can only be swapped horizontally or vertically into the free space. Puzzle pieces cannot be swapped with each
  other directly.
</p>

<p>
  Each run is evaluated by two criterias: how fast and how efficient you managed to solve the puzzle. You can try to
  speedrun the game by using less time and clicking faster, or take more time - but solve the game in less actions
  (swaps).
</p>

<p>
  Check out the <i>settings menu</i> if you need to amp up the accessibility for the game.
</p>

